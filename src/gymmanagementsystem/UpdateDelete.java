    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int checkid = 0;
        String id = jTextField2.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from member where id='" + id + "'");
            while (rs.next()) {
                checkid = 1;

                jTextField1.setText(rs.getString(2));
                jTextField6.setText(rs.getString(3));
                jTextField10.setText(rs.getString(4));

                jTextField11.setText(rs.getString(5));
                jTextField3.setText(rs.getString(6));
                jTextField4.setText(rs.getString(7));

                jTextField5.setText(rs.getString(8));
                jTextField7.setText(rs.getString(9));
                jTextField7.setEditable(false);
                jTextField9.setText(rs.getString(10));
                jTextField9.setEditable(false);
                jTextField8.setText(rs.getString(11));
            }
            if (checkid == 0) {
                JOptionPane.showMessageDialog(null, "Member ID does not Exist");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

