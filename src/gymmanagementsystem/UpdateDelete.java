    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField2.getText();
        String name = jTextField1.getText();
        String lastname = jTextField6.getText();
        String cin = jTextField10.getText();
        String address = jTextField11.getText();
        String email = jTextField3.getText();
        String number = jTextField4.getText();
        String age = jTextField10.getText();
        String amount = jTextField8.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("update member set name=?,lastname=?,cin=?,address=?,email=?,phone=?,age=?,amount=? where id=?");
            ps.setString(1, name);
            ps.setString(2, lastname);
            ps.setString(3, cin);
            ps.setString(4, address);
            ps.setString(5, email);
            ps.setString(6, number);
            ps.setString(7, age);
            ps.setString(8, amount);
            ps.setString(9, id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new UpdateDelete().setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

